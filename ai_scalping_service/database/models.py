    # ==================== Analysis Queries ====================
    
    def get_win_rate_by_momentum_range(self) -> List[Dict]:
        """Analyze win rate by momentum score ranges"""
        cursor = self.conn.cursor()
        cursor.execute("""
            SELECT 
                CASE 
                    WHEN momentum_at_entry < 60 THEN '50-60'
                    WHEN momentum_at_entry < 70 THEN '60-70'
                    WHEN momentum_at_entry < 80 THEN '70-80'
                    WHEN momentum_at_entry < 90 THEN '80-90'
                    ELSE '90-100'
                END as momentum_range,
                COUNT(*) as total_trades,
                SUM(CASE WHEN pnl > 0 THEN 1 ELSE 0 END) as wins,
                ROUND(SUM(CASE WHEN pnl > 0 THEN 1.0 ELSE 0 END) / COUNT(*) * 100, 2) as win_rate,
                ROUND(AVG(pnl), 2) as avg_pnl
            FROM paper_trades 
            WHERE status = 'exited'
            GROUP BY momentum_range
            ORDER BY momentum_range
        """)
        return [dict(row) for row in cursor.fetchall()]
    
    def get_best_performing_instruments(self) -> List[Dict]:
        """Get instruments ranked by performance"""
        cursor = self.conn.cursor()
        cursor.execute("""
            SELECT 
                instrument,
                COUNT(*) as total_trades,
                SUM(CASE WHEN pnl > 0 THEN 1 ELSE 0 END) as wins,
                ROUND(SUM(CASE WHEN pnl > 0 THEN 1.0 ELSE 0 END) / COUNT(*) * 100, 2) as win_rate,
                ROUND(SUM(pnl), 2) as total_pnl,
                ROUND(AVG(pnl), 2) as avg_pnl
            FROM paper_trades 
            WHERE status = 'exited'
            GROUP BY instrument
            ORDER BY win_rate DESC
        """)
        return [dict(row) for row in cursor.fetchall()]
    
    def get_optimal_entry_conditions(self) -> Dict:
        """Analyze what entry conditions lead to best outcomes"""
        cursor = self.conn.cursor()
        
        # Best momentum score range
        cursor.execute("""
            SELECT 
                ROUND(momentum_at_entry / 5) * 5 as momentum_bucket,
                ROUND(AVG(pnl), 2) as avg_pnl,
                COUNT(*) as trades
            FROM paper_trades WHERE status = 'exited'
            GROUP BY momentum_bucket
            ORDER BY avg_pnl DESC
            LIMIT 1
        """)
        best_momentum = cursor.fetchone()
        
        # Best time of day
        cursor.execute("""
            SELECT 
                strftime('%H', timestamp_entry) as hour,
                ROUND(AVG(pnl), 2) as avg_pnl,
                COUNT(*) as trades
            FROM paper_trades WHERE status = 'exited'
            GROUP BY hour
            ORDER BY avg_pnl DESC
            LIMIT 1
        """)
        best_hour = cursor.fetchone()
        
        return {
            'best_momentum_range': dict(best_momentum) if best_momentum else None,
            'best_trading_hour': dict(best_hour) if best_hour else None
        }
    
    def get_analysis_summary(self) -> Dict:
        """Get comprehensive analysis summary for Copilot"""
        cursor = self.conn.cursor()
        
        summary = {}
        
        # Overall stats
        cursor.execute("""
            SELECT 
                COUNT(*) as total_trades,
                SUM(CASE WHEN pnl > 0 THEN 1 ELSE 0 END) as wins,
                ROUND(SUM(CASE WHEN pnl > 0 THEN 1.0 ELSE 0 END) / NULLIF(COUNT(*), 0) * 100, 2) as win_rate,
                ROUND(SUM(pnl), 2) as total_pnl,
                ROUND(AVG(pnl), 2) as avg_pnl,
                ROUND(AVG(duration_seconds), 0) as avg_duration_sec
            FROM paper_trades WHERE status = 'exited'
        """)
        summary['overall'] = dict(cursor.fetchone())
        
        # By instrument
        summary['by_instrument'] = self.get_best_performing_instruments()
        
        # By momentum
        summary['by_momentum'] = self.get_win_rate_by_momentum_range()
        
        # Recent performance trend
        cursor.execute("""
            SELECT 
                DATE(timestamp_entry) as trade_date,
                COUNT(*) as trades,
                ROUND(SUM(CASE WHEN pnl > 0 THEN 1.0 ELSE 0 END) / COUNT(*) * 100, 2) as win_rate,
                ROUND(SUM(pnl), 2) as pnl
            FROM paper_trades 
            WHERE status = 'exited' AND timestamp_entry >= date('now', '-7 days')
            GROUP BY trade_date
            ORDER BY trade_date
        """)
        summary['recent_trend'] = [dict(row) for row in cursor.fetchall()]
        
        # Optimal conditions
        summary['optimal_conditions'] = self.get_optimal_entry_conditions()
        
        return summary
    
