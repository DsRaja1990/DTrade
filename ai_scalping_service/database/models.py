class ScalpingDatabase:
    """SQLite database manager for AI Scalping Service"""
    
    def __init__(self, db_path: str = None):
        if db_path is None:
            db_dir = Path(__file__).parent
            db_path = str(db_dir / "scalping_data.db")
        
        self.db_path = db_path
        self._local = threading.local()
        self._init_database()
    
    @property
    def conn(self) -> sqlite3.Connection:
        """Thread-local database connection"""
        if not hasattr(self._local, 'conn') or self._local.conn is None:
            self._local.conn = sqlite3.connect(self.db_path, check_same_thread=False)
            self._local.conn.row_factory = sqlite3.Row
        return self._local.conn
    
    def _init_database(self):
        """Initialize all database tables"""
        cursor = self.conn.cursor()
        
        # Trading mode table
        cursor.execute("""
            CREATE TABLE IF NOT EXISTS trading_mode (
                id INTEGER PRIMARY KEY,
                mode TEXT NOT NULL DEFAULT 'paper',
                updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
                updated_by TEXT DEFAULT 'system'
            )
        """)
        
        # Service state table - for persistent strategy enabled/disabled state
        cursor.execute("""
            CREATE TABLE IF NOT EXISTS service_state (
                key TEXT PRIMARY KEY,
                value TEXT NOT NULL,
                updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
            )
        """)
        
        # Market ticks table - for historical analysis
        cursor.execute("""
            CREATE TABLE IF NOT EXISTS market_ticks (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                timestamp TIMESTAMP NOT NULL,
                instrument TEXT NOT NULL,
                symbol TEXT,
                ltp REAL NOT NULL,
                open REAL,
                high REAL,
                low REAL,
                close REAL,
                volume INTEGER,
                oi INTEGER,
                bid REAL,
                ask REAL,
                bid_qty INTEGER,
                ask_qty INTEGER,
                created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
            )
        """)
        
        # Momentum snapshots - track momentum evolution
        cursor.execute("""
            CREATE TABLE IF NOT EXISTS momentum_snapshots (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                timestamp TIMESTAMP NOT NULL,
                instrument TEXT NOT NULL,
                score REAL,
                velocity REAL,
                acceleration REAL,
                phase TEXT,
                consecutive_up INTEGER,
                consecutive_down INTEGER,
                streak_strength REAL,
                volume_ratio REAL,
                created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
            )
        """)
        
        # Signals table - all signals generated
        cursor.execute("""
            CREATE TABLE IF NOT EXISTS signals (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                timestamp TIMESTAMP NOT NULL,
                instrument TEXT NOT NULL,
                signal_type TEXT NOT NULL,
                direction TEXT,
                strength REAL,
                momentum_score REAL,
                ai_confidence REAL,
                reason TEXT,
                was_executed INTEGER DEFAULT 0,
                execution_result TEXT,
                created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
            )
        """)
        
        # Paper trades table - simulated trades
        cursor.execute("""
            CREATE TABLE IF NOT EXISTS paper_trades (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                trade_id TEXT UNIQUE NOT NULL,
                timestamp_entry TIMESTAMP NOT NULL,
                timestamp_exit TIMESTAMP,
                instrument TEXT NOT NULL,
                option_type TEXT,
                strike REAL,
                entry_price REAL NOT NULL,
                exit_price REAL,
                quantity INTEGER NOT NULL,
                lot_size INTEGER,
                pnl REAL DEFAULT 0,
                pnl_percent REAL DEFAULT 0,
                status TEXT DEFAULT 'entered',
                entry_reason TEXT,
                exit_reason TEXT,
                momentum_at_entry REAL,
                momentum_at_exit REAL,
                ai_confidence_entry REAL,
                duration_seconds INTEGER,
                max_profit REAL DEFAULT 0,
                max_loss REAL DEFAULT 0,
                created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
            )
        """)
        
        # Daily statistics - aggregated daily performance
        cursor.execute("""
            CREATE TABLE IF NOT EXISTS daily_stats (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                date DATE UNIQUE NOT NULL,
                total_trades INTEGER DEFAULT 0,
                winning_trades INTEGER DEFAULT 0,
                losing_trades INTEGER DEFAULT 0,
                win_rate REAL DEFAULT 0,
                total_pnl REAL DEFAULT 0,
                max_drawdown REAL DEFAULT 0,
                profit_factor REAL DEFAULT 0,
                avg_win REAL DEFAULT 0,
                avg_loss REAL DEFAULT 0,
                best_trade REAL DEFAULT 0,
                worst_trade REAL DEFAULT 0,
                total_signals INTEGER DEFAULT 0,
                executed_signals INTEGER DEFAULT 0,
                signal_accuracy REAL DEFAULT 0,
                avg_momentum_at_entry REAL DEFAULT 0,
                avg_ai_confidence REAL DEFAULT 0,
                created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
                updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
            )
        """)
        
        # Strategy configurations - track config changes
        cursor.execute("""
            CREATE TABLE IF NOT EXISTS strategy_configs (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                timestamp TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
                config_json TEXT NOT NULL,
                performance_before REAL DEFAULT 0,
                performance_after REAL DEFAULT 0,
                is_active INTEGER DEFAULT 1,
                notes TEXT
            )
        """)
        
        # Learning insights - AI-generated recommendations
        cursor.execute("""
            CREATE TABLE IF NOT EXISTS learning_insights (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                timestamp TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
                insight_type TEXT NOT NULL,
                description TEXT NOT NULL,
                recommendation TEXT,
                confidence REAL DEFAULT 0,
                implemented INTEGER DEFAULT 0,
                result TEXT
            )
        """)
        
        # Option chain snapshots - for analysis
        cursor.execute("""
            CREATE TABLE IF NOT EXISTS option_chain (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                timestamp TIMESTAMP NOT NULL,
                instrument TEXT NOT NULL,
                expiry DATE NOT NULL,
                strike REAL NOT NULL,
                option_type TEXT NOT NULL,
                ltp REAL,
                iv REAL,
                delta REAL,
                gamma REAL,
                theta REAL,
                vega REAL,
                oi INTEGER,
                volume INTEGER,
                bid REAL,
                ask REAL,
                created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
            )
        """)
        
        # Performance metrics - for tracking improvement over time
        cursor.execute("""
            CREATE TABLE IF NOT EXISTS performance_metrics (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                timestamp TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
                metric_name TEXT NOT NULL,
                metric_value REAL NOT NULL,
                period TEXT,
                notes TEXT
            )
        """)
        
        # Create indexes for faster queries
        cursor.execute("CREATE INDEX IF NOT EXISTS idx_ticks_timestamp ON market_ticks(timestamp)")
        cursor.execute("CREATE INDEX IF NOT EXISTS idx_ticks_instrument ON market_ticks(instrument)")
        cursor.execute("CREATE INDEX IF NOT EXISTS idx_momentum_timestamp ON momentum_snapshots(timestamp)")
        cursor.execute("CREATE INDEX IF NOT EXISTS idx_signals_timestamp ON signals(timestamp)")
        cursor.execute("CREATE INDEX IF NOT EXISTS idx_trades_status ON paper_trades(status)")
        cursor.execute("CREATE INDEX IF NOT EXISTS idx_daily_date ON daily_stats(date)")
        
        # Initialize trading mode if not exists
        cursor.execute("SELECT COUNT(*) FROM trading_mode")
        if cursor.fetchone()[0] == 0:
            cursor.execute("INSERT INTO trading_mode (mode) VALUES ('paper')")
        
        self.conn.commit()
    
    # ==================== Trading Mode ====================
    
    def get_trading_mode(self) -> TradeMode:
        """Get current trading mode"""
        cursor = self.conn.cursor()
        cursor.execute("SELECT mode FROM trading_mode ORDER BY id DESC LIMIT 1")
        row = cursor.fetchone()
        return TradeMode(row['mode']) if row else TradeMode.PAPER
    
    def set_trading_mode(self, mode: TradeMode, updated_by: str = "api") -> bool:
        """Set trading mode (paper or production)"""
        cursor = self.conn.cursor()
        cursor.execute("""
            UPDATE trading_mode SET mode = ?, updated_at = ?, updated_by = ?
            WHERE id = (SELECT MAX(id) FROM trading_mode)
        """, (mode.value, datetime.now(), updated_by))
        self.conn.commit()
        return cursor.rowcount > 0
    
    # ==================== Market Ticks ====================
    
    def insert_tick(self, tick: MarketTick) -> int:
        """Insert a market tick"""
        cursor = self.conn.cursor()
        cursor.execute("""
            INSERT INTO market_ticks 
            (timestamp, instrument, symbol, ltp, open, high, low, close, volume, oi, bid, ask, bid_qty, ask_qty)
            VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
        """, (tick.timestamp, tick.instrument, tick.symbol, tick.ltp, tick.open, 
              tick.high, tick.low, tick.close, tick.volume, tick.oi,
              tick.bid, tick.ask, tick.bid_qty, tick.ask_qty))
        self.conn.commit()
        return cursor.lastrowid
    
    def insert_ticks_batch(self, ticks: List[MarketTick]) -> int:
        """Insert multiple ticks efficiently"""
        cursor = self.conn.cursor()
        data = [(t.timestamp, t.instrument, t.symbol, t.ltp, t.open, t.high, t.low, 
                 t.close, t.volume, t.oi, t.bid, t.ask, t.bid_qty, t.ask_qty) for t in ticks]
        cursor.executemany("""
            INSERT INTO market_ticks 
            (timestamp, instrument, symbol, ltp, open, high, low, close, volume, oi, bid, ask, bid_qty, ask_qty)
            VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
        """, data)
        self.conn.commit()
        return len(data)
    
    def get_ticks(self, instrument: str, start: datetime, end: datetime) -> List[Dict]:
        """Get ticks for an instrument in a time range"""
        cursor = self.conn.cursor()
        cursor.execute("""
            SELECT * FROM market_ticks 
            WHERE instrument = ? AND timestamp BETWEEN ? AND ?
            ORDER BY timestamp
        """, (instrument, start, end))
        return [dict(row) for row in cursor.fetchall()]
    
    # ==================== Momentum Snapshots ====================
    
    def insert_momentum(self, momentum: MomentumSnapshot) -> int:
        """Insert momentum snapshot"""
        cursor = self.conn.cursor()
        cursor.execute("""
            INSERT INTO momentum_snapshots 
            (timestamp, instrument, score, velocity, acceleration, phase, 
             consecutive_up, consecutive_down, streak_strength, volume_ratio)
            VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
        """, (momentum.timestamp, momentum.instrument, momentum.score, momentum.velocity,
              momentum.acceleration, momentum.phase, momentum.consecutive_up,
              momentum.consecutive_down, momentum.streak_strength, momentum.volume_ratio))
        self.conn.commit()
        return cursor.lastrowid
    
    def get_momentum_history(self, instrument: str, hours: int = 24) -> List[Dict]:
        """Get momentum history for analysis"""
        cursor = self.conn.cursor()
        cursor.execute("""
            SELECT * FROM momentum_snapshots 
            WHERE instrument = ? AND timestamp >= datetime('now', ? || ' hours')
            ORDER BY timestamp
        """, (instrument, -hours))
        return [dict(row) for row in cursor.fetchall()]
    
    # ==================== Signals ====================
    
    def insert_signal(self, signal: Signal) -> int:
        """Insert a trading signal"""
        cursor = self.conn.cursor()
        cursor.execute("""
            INSERT INTO signals 
            (timestamp, instrument, signal_type, direction, strength, momentum_score, 
             ai_confidence, reason, was_executed, execution_result)
            VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
        """, (signal.timestamp, signal.instrument, signal.signal_type, signal.direction,
              signal.strength, signal.momentum_score, signal.ai_confidence, signal.reason,
              1 if signal.was_executed else 0, signal.execution_result))
        self.conn.commit()
        return cursor.lastrowid
    
    def update_signal_execution(self, signal_id: int, was_executed: bool, result: str):
        """Update signal execution status"""
        cursor = self.conn.cursor()
        cursor.execute("""
            UPDATE signals SET was_executed = ?, execution_result = ? WHERE id = ?
        """, (1 if was_executed else 0, result, signal_id))
        self.conn.commit()
    
    def get_signals_by_date(self, target_date: date) -> List[Dict]:
        """Get all signals for a date"""
