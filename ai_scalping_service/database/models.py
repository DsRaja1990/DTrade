    # ==================== Daily Stats ====================
    
    def update_daily_stats(self, target_date: date = None):
        """Calculate and update daily statistics"""
        if target_date is None:
            target_date = date.today()
        
        cursor = self.conn.cursor()
        
        # Get all trades for the date
        cursor.execute("""
            SELECT * FROM paper_trades 
            WHERE DATE(timestamp_entry) = ? AND status = 'exited'
        """, (target_date,))
        trades = cursor.fetchall()
        
        if not trades:
            return
        
        # Calculate statistics
        total_trades = len(trades)
        winning_trades = sum(1 for t in trades if t['pnl'] > 0)
        losing_trades = sum(1 for t in trades if t['pnl'] <= 0)
        win_rate = winning_trades / total_trades * 100 if total_trades > 0 else 0
        
        total_pnl = sum(t['pnl'] for t in trades)
        
        wins = [t['pnl'] for t in trades if t['pnl'] > 0]
        losses = [abs(t['pnl']) for t in trades if t['pnl'] < 0]
        
        avg_win = sum(wins) / len(wins) if wins else 0
        avg_loss = sum(losses) / len(losses) if losses else 0
        profit_factor = sum(wins) / sum(losses) if losses and sum(losses) > 0 else 999
        
        best_trade = max(t['pnl'] for t in trades)
        worst_trade = min(t['pnl'] for t in trades)
        
        avg_momentum = sum(t['momentum_at_entry'] for t in trades) / total_trades
        avg_confidence = sum(t['ai_confidence_entry'] or 0 for t in trades) / total_trades
        
        # Get signals
        cursor.execute("SELECT COUNT(*) FROM signals WHERE DATE(timestamp) = ?", (target_date,))
        total_signals = cursor.fetchone()[0]
        
        cursor.execute("SELECT COUNT(*) FROM signals WHERE DATE(timestamp) = ? AND was_executed = 1", (target_date,))
        executed_signals = cursor.fetchone()[0]
        
        # Upsert daily stats
        cursor.execute("""
            INSERT INTO daily_stats 
            (date, total_trades, winning_trades, losing_trades, win_rate, total_pnl, 
             profit_factor, avg_win, avg_loss, best_trade, worst_trade, 
             total_signals, executed_signals, avg_momentum_at_entry, avg_ai_confidence, updated_at)
            VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
            ON CONFLICT(date) DO UPDATE SET
                total_trades = excluded.total_trades,
                winning_trades = excluded.winning_trades,
                losing_trades = excluded.losing_trades,
                win_rate = excluded.win_rate,
                total_pnl = excluded.total_pnl,
                profit_factor = excluded.profit_factor,
                avg_win = excluded.avg_win,
                avg_loss = excluded.avg_loss,
                best_trade = excluded.best_trade,
                worst_trade = excluded.worst_trade,
                total_signals = excluded.total_signals,
                executed_signals = excluded.executed_signals,
                avg_momentum_at_entry = excluded.avg_momentum_at_entry,
                avg_ai_confidence = excluded.avg_ai_confidence,
                updated_at = excluded.updated_at
        """, (target_date, total_trades, winning_trades, losing_trades, win_rate, total_pnl,
              profit_factor, avg_win, avg_loss, best_trade, worst_trade,
              total_signals, executed_signals, avg_momentum, avg_confidence, datetime.now()))
        self.conn.commit()
    
    def get_daily_stats(self, days: int = 30) -> List[Dict]:
        """Get daily statistics for the last N days"""
        cursor = self.conn.cursor()
        cursor.execute("""
            SELECT * FROM daily_stats 
            ORDER BY date DESC LIMIT ?
        """, (days,))
        return [dict(row) for row in cursor.fetchall()]
    
    # ==================== Strategy Configs ====================
    
    def save_strategy_config(self, config: Dict, notes: str = "") -> int:
        """Save a strategy configuration"""
        cursor = self.conn.cursor()
        # Deactivate previous configs
        cursor.execute("UPDATE strategy_configs SET is_active = 0")
        
        cursor.execute("""
            INSERT INTO strategy_configs (config_json, is_active, notes)
            VALUES (?, 1, ?)
        """, (json.dumps(config), notes))
        self.conn.commit()
        return cursor.lastrowid
    
    def get_active_config(self) -> Optional[Dict]:
        """Get the active strategy configuration"""
        cursor = self.conn.cursor()
        cursor.execute("SELECT * FROM strategy_configs WHERE is_active = 1 ORDER BY id DESC LIMIT 1")
        row = cursor.fetchone()
        if row:
            result = dict(row)
            result['config'] = json.loads(result['config_json'])
            return result
        return None
    
    # ==================== Learning Insights ====================
    
    def add_insight(self, insight: LearningInsight) -> int:
        """Add a learning insight"""
        cursor = self.conn.cursor()
        cursor.execute("""
            INSERT INTO learning_insights 
            (insight_type, description, recommendation, confidence)
            VALUES (?, ?, ?, ?)
        """, (insight.insight_type, insight.description, insight.recommendation, insight.confidence))
        self.conn.commit()
        return cursor.lastrowid
    
