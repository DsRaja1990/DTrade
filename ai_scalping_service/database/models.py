"""
SQLite Database Models for AI Scalping Service
Stores all data needed for strategy analysis, learning, and self-improvement
"""

import sqlite3
from datetime import datetime, date
from pathlib import Path
from typing import List, Dict, Any, Optional, Tuple
from dataclasses import dataclass, field, asdict
from enum import Enum
import json
import threading


class TradeMode(Enum):
    """Trading execution mode"""
    PAPER = "paper"  # Simulated trades, no real money
    PRODUCTION = "production"  # Real trades with money


class TradeStatus(Enum):
    """Trade lifecycle status"""
    PENDING = "pending"
    ENTERED = "entered"
    PARTIAL_EXIT = "partial_exit"
    EXITED = "exited"
    CANCELLED = "cancelled"
    FAILED = "failed"


class SignalType(Enum):
    """Signal types for analysis"""
    MOMENTUM_ENTRY = "momentum_entry"
    MOMENTUM_EXIT = "momentum_exit"
    AI_OVERRIDE = "ai_override"
    STOP_LOSS = "stop_loss"
    TARGET_HIT = "target_hit"
    TIME_EXIT = "time_exit"
    CONSECUTIVE_TICK = "consecutive_tick"


